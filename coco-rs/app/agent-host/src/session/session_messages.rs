pub fn model_role_change_messages(
    changed: &coco_types::ModelRoleChangedParams,
    display_name: &str,
    is_remote: bool,
) -> Vec<coco_messages::Message> {
    let role_label = title_case_model_role(changed.role);
    let effort_suffix = changed
        .effort
        .map(|effort| format!(" · thinking: {effort}"))
        .unwrap_or_default();
    let display_label = if changed.provider == "moa" {
        format!("{}/{}", changed.provider, changed.model_id)
    } else {
        format!("{}/{}", changed.provider, display_name)
    };
    let output = format!("Set {role_label} → {display_label}{effort_suffix}");
    let mut messages = slash_text_messages(
        "model", /*args*/ "", &output, /*is_sensitive*/ false,
    );
    if let Some(reminder) =
        remote_model_change_reminder_message(changed.role, display_name, is_remote)
    {
        messages.push(reminder);
    }
    messages
}

pub async fn append_model_role_change_to_history_and_emit(
    session: &SessionHandle,
    event_tx: tokio::sync::mpsc::Sender<coco_types::CoreEvent>,
    changed: &coco_types::ModelRoleChangedParams,
    display_name: &str,
    is_remote: bool,
) -> Vec<Arc<coco_messages::Message>> {
    session
        .append_messages_to_history_and_emit(
            model_role_change_messages(changed, display_name, is_remote),
            Some(event_tx),
        )
        .await
}

pub fn remote_model_change_reminder_message(
    role: coco_types::ModelRole,
    display_name: &str,
    is_remote: bool,
) -> Option<coco_messages::Message> {
    if !is_remote || role != coco_types::ModelRole::Main {
        return None;
    }
    Some(coco_messages::wrapping::create_system_reminder_message(
        &format!(
            "The model for this session has been changed to {display_name}. You are now running as {display_name}."
        ),
    ))
}

fn title_case_model_role(role: coco_types::ModelRole) -> String {
    let mut chars = role.as_str().chars();
    chars.next().map_or_else(String::new, |first| {
        format!("{}{}", first.to_uppercase(), chars.as_str())
    })
}

