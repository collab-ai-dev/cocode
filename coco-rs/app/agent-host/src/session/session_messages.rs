pub fn local_command_messages(
    command: &str,
    output: &str,
    should_respond: bool,
) -> Vec<coco_messages::Message> {
    let mut messages = Vec::new();
    if !should_respond {
        messages.push(coco_messages::create_meta_message(
            "<local-command-caveat>Caveat: The messages below were generated by the user while running local commands. DO NOT respond to these messages or otherwise consider them in your response unless the user explicitly asks you to.</local-command-caveat>",
        ));
    }
    messages.push(coco_messages::Message::System(
        coco_messages::SystemMessage::LocalCommand(coco_messages::SystemLocalCommandMessage {
            uuid: uuid::Uuid::new_v4(),
            command: command.to_string(),
            output: output.to_string(),
        }),
    ));
    messages
}

pub async fn append_local_command_to_history_and_emit(
    session: &SessionHandle,
    event_tx: tokio::sync::mpsc::Sender<coco_types::CoreEvent>,
    command: &str,
    output: &str,
    should_respond: bool,
) -> Vec<Arc<coco_messages::Message>> {
    session
        .append_messages_to_history_and_emit(
            local_command_messages(command, output, should_respond),
            Some(event_tx),
        )
        .await
}

pub fn compact_summary_message(summary: &str) -> Option<coco_messages::Message> {
    if summary.trim().is_empty() {
        return None;
    }
    Some(coco_compact::build_compact_summary_message(summary))
}

pub async fn append_compact_summary_to_history_and_emit(
    session: &SessionHandle,
    event_tx: tokio::sync::mpsc::Sender<coco_types::CoreEvent>,
    summary: &str,
) -> Option<Vec<Arc<coco_messages::Message>>> {
    let message = compact_summary_message(summary)?;
    Some(
        session
            .append_messages_to_history_and_emit(vec![message], Some(event_tx))
            .await,
    )
}

