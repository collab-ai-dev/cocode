use std::sync::Arc;

use crate::session_runtime::SessionHandle;

pub fn slash_command_metadata(name: &str, args: &str) -> String {
    let mut body =
        format!("<command-message>{name}</command-message>\n<command-name>/{name}</command-name>");
    let trimmed_args = args.trim();
    if !trimmed_args.is_empty() {
        body.push_str(&format!("\n<command-args>{trimmed_args}</command-args>"));
    }
    body
}

pub fn slash_metadata_message(metadata: &str) -> coco_messages::Message {
    let attachment = coco_messages::AttachmentMessage::api(
        coco_types::AttachmentKind::SlashCommandMetadata,
        coco_messages::LlmMessage::user_text(metadata),
    );
    coco_messages::Message::Attachment(attachment)
}

pub fn slash_text_messages(
    command: &str,
    args: &str,
    text: &str,
    is_sensitive: bool,
) -> Vec<coco_messages::Message> {
    coco_messages::build_slash_command_messages(command, args, text, is_sensitive)
}

pub fn slash_result_messages(
    command: &str,
    args: &str,
    text: &str,
    is_error: bool,
) -> Vec<coco_messages::Message> {
    if is_error {
        coco_messages::build_slash_command_error_messages(command, args, text)
    } else {
        slash_text_messages(command, args, text, /*is_sensitive*/ false)
    }
}

pub async fn append_slash_result_to_history_and_emit(
    session: &SessionHandle,
    event_tx: tokio::sync::mpsc::Sender<coco_types::CoreEvent>,
    command: &str,
    args: &str,
    text: &str,
    is_error: bool,
) -> Vec<Arc<coco_messages::Message>> {
    session
        .append_messages_to_history_and_emit(
            slash_result_messages(command, args, text, is_error),
            Some(event_tx),
        )
        .await
}

